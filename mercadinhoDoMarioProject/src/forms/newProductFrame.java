    private void btnLoadProductImgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadProductImgActionPerformed
        JFileChooser fc = new JFileChooser();
        FileFilter ff = new FileNameExtensionFilter("images","jpeg");
        fc.addChoosableFileFilter(ff);
        int open = fc.showOpenDialog(this);
        if (open == javax.swing.JFileChooser.APPROVE_OPTION){
            java.io.File path = fc.getSelectedFile();
            String file_name = path.toString();
            BufferedImage bi;
            try{
                bi = ImageIO.read(path);
                Image resizedImg = bi.getScaledInstance(100, 110, Image.SCALE_SMOOTH);
                ImageIcon iconImg = new ImageIcon(resizedImg);
                imgProductImage.setIcon(iconImg);
                
                String fileUploadedDestiny = "";
                Instant instant = Instant.now();
                    long timestamp = instant.toEpochMilli();
                    txtCode.setText("" + timestamp);
                    fileUploadedDestiny = "../productsImg/" + timestamp + "/";
                
                File fileUploadedDestinyFolder = new File(fileUploadedDestiny);
                if (!fileUploadedDestinyFolder.exists()) {
                    fileUploadedDestinyFolder.mkdirs();
                }
                
                Path originFile = path.toPath();
                String originalFileName = originFile.getFileName().toString();
                
                String fileExtension = originalFileName.substring(originalFileName.lastIndexOf('.'));
                String renamedFileName =  formatToCamelCase(txtName.getText()) + fileExtension;
                
                Path destinyFile = fileUploadedDestinyFolder.toPath().resolve(renamedFileName);
                txtProductImgPath.setText(destinyFile.toString().replace("\\", "__"));

                btnLoadProductImg.setText("Trocar imagem");
                validateProductImgChange = 1;
                originFileForFinalInsert = originFile;
                destinyFileForFinalInsert = destinyFile;
            }
            catch(IOException e){
            }
        }
    }//GEN-LAST:event_btnLoadProductImgActionPerformed

