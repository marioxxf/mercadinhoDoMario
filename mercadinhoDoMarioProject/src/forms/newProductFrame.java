    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if(txtName.getText().length() > 0 && txtDescription.getText().length() > 0 && txtEanCode.getText().length() > 0 
                && txtNcm.getText().length() > 0 && txtStockQtd.getText().length() > 0 && txtRegisterDate.getText().length() > 0 
                && txtCode.getText().length() > 0 && txtStatus.getText().length() > 0 && txtMinStockQtd.getText().length() > 0 
                && txtMaxStockQtd.getText().length() > 0 && txtBuyAmount.getText().length() > 0 && txtSaleAmount.getText().length() > 0
                && txtProductImgPath.getText().length() > 0 && txtProfitAmount.getText().length() > 0 && txtCode.getText().length() > 0)
        {
            try {
                Files.copy(originFileForFinalInsert, destinyFileForFinalInsert, StandardCopyOption.REPLACE_EXISTING);
            } catch (IOException ex) {
                System.out.println("Erro!");
            }
            BigInteger eanCodeForInsert = new BigInteger(txtEanCode.getText());
            bdManager bdManagerInstance = new bdManager();
            bdManagerInstance.dynamicAddProduct(txtCode.getText(), txtStatus.getText(), txtName.getText(), txtDescription.getText(), Integer.parseInt(txtStockQtd.getText()), Integer.parseInt(txtMinStockQtd.getText()), Integer.parseInt(txtMaxStockQtd.getText()), Float.parseFloat(txtBuyAmount.getText()), Float.parseFloat(txtSaleAmount.getText()), eanCodeForInsert, Integer.parseInt(txtNcm.getText()), Float.parseFloat(txtProfitAmount.getText().replace(",", ".")), txtProductImgPath.getText());
            JOptionPane.showMessageDialog(null, "O produto " + txtName.getText() + " foi cadastrado com sucesso!");
            this.dispose();
            mainFrame mainFrameInstance = new mainFrame();
            mainFrameInstance.setLocationRelativeTo(null);
            mainFrameInstance.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Registro nÃ£o concluÃ­do! Certifique-se de que preencheu TODOS os campos da tela.");
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

    private void txtBuyAmountFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtBuyAmountFocusLost
        if(txtBuyAmount.getText().length() > 0 && txtSaleAmount.getText().length() > 0){
            DecimalFormat df = new DecimalFormat("0.00");
            float profitAmount = ((((Float.parseFloat(txtSaleAmount.getText()) - Float.parseFloat(txtBuyAmount.getText()))/Float.parseFloat(txtBuyAmount.getText()))*100));
            String profitAmountFormatted = df.format(profitAmount);
            txtProfitAmount.setText(profitAmountFormatted);
        }
    }//GEN-LAST:event_txtBuyAmountFocusLost

