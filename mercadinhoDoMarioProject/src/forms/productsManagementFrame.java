    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int awnswer = JOptionPane.showOptionDialog(
                null,
                "Você realmente quer apagar o produto " + txtProductTitle.getText() + " da base de dados do sistema?",
                "Confirmação de exclusão",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                new Object[]{"Sim", "Não"},
                "Sim");

        if (awnswer == JOptionPane.YES_OPTION) {
            bdManagerInstance.deleteProduct(Integer.parseInt(txtProductId.getText()));
            JOptionPane.showMessageDialog(null, "Produto excluído com sucesso!");
            this.dispose();
            mainFrame mainFrameInstance = new mainFrame();
            mainFrameInstance.setLocationRelativeTo(null);
            mainFrameInstance.setVisible(true);
        } else if (awnswer == JOptionPane.NO_OPTION) {
        } else if (awnswer == JOptionPane.CLOSED_OPTION) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

