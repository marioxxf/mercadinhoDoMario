    public void searchProductById(int productIdSearched) throws IOException{
        System.out.println(products);
        for(int x = 0; x < products.size(); x++){
            if(Integer.parseInt(products.get(x).get(0).toString()) == productIdSearched){
                activeProduct = products.indexOf(products.get(x));
            }
        }
        populateActiveProduct("searchById");
    }
    
    public void populateActiveProduct(String direction) throws IOException{
        if(products.size() > 0){
            if(direction == "searchById"){
            activeProduct = activeProduct;
            } else {
                if(direction == "initial"){
                activeProduct = 0;
                }

                if(direction == "right"){
                    if(activeProduct == (products.size()-1)){
                        activeProduct = 0;
                    } else{
                        activeProduct = activeProduct+1;
                    }
                } else if(direction == "left"){
                    if(activeProduct == 0){
                        activeProduct = (products.size()-1);
                    } else{
                        activeProduct = activeProduct-1;
                    }
                }
            }
            
            DateTimeFormatter inputFormat = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
            DateTimeFormatter outputFormat = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDateTime dateTimeFormat = LocalDateTime.parse(products.get(activeProduct).get(13).toString(), inputFormat);
            String registerDateFormated = dateTimeFormat.format(outputFormat);

            txtProductId.setText(products.get(activeProduct).get(0).toString());
            txtCode.setText(products.get(activeProduct).get(1).toString());
            txtStatus.setText(products.get(activeProduct).get(2).toString());
            txtName.setText(products.get(activeProduct).get(3).toString());
            txtDescription.setText(products.get(activeProduct).get(4).toString());
            txtStockQtd.setText(products.get(activeProduct).get(5).toString());
            txtMinStockQtd.setText(products.get(activeProduct).get(6).toString());
            txtMaxStockQtd.setText(products.get(activeProduct).get(7).toString());
            txtBuyAmount.setText(products.get(activeProduct).get(8).toString());
            txtSaleAmount.setText(products.get(activeProduct).get(9).toString());
            txtEanCode.setText(products.get(activeProduct).get(10).toString());
            txtNcm.setText(products.get(activeProduct).get(11).toString());
            txtProfitAmount.setText(products.get(activeProduct).get(12).toString());
            txtRegisterDate.setText(registerDateFormated);
            txtProductTitle.setText(txtName.getText());

            String productImage = products.get(activeProduct).get(14).toString().replace("__", "/");
            File file = new File(productImage);
            BufferedImage bi;
            bi = ImageIO.read(file);
            Image resizedImg = bi.getScaledInstance(100, 110, Image.SCALE_SMOOTH);
            ImageIcon iconImg = new ImageIcon(resizedImg);
            imgProductImage.setIcon(iconImg);
            txtNowProduct.setText(Integer.toString((activeProduct+1)) + "/" + Integer.toString(products.size()));
        }
    }
